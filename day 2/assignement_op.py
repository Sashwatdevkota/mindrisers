#operaters used to assign value to a variable


#add and assign (+=)
a = 15 
print("for addition")
a += 5     # a = a+5 
print(a)

#subtract and assign (-=)
a = 15 
print("for subtraction")
a -= 5  # a=a-5
print(a)

# *=
a = 15 
print("for multiplication")
a *= 5  # a=a*5
print(a)

# /=
a = 15 
print("for division")
a /= 5  # a=a/5
print(a)


# //=
a = 15 
print("for floor division")
a //= 5  # a=a//5
print(a)

# %=
a = 15 
print("for module")
a %= 5  # a=a%5
print(a)

# **=
a = 15 
print("for exponentation")
a **= 5  # a=a**5
print(a)


